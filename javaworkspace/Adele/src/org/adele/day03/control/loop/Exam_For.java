package org.adele.day03.control.loop;

public class Exam_For {
	public static void main(String [] args) {
		// 반복문
		// 실행문장을 원하는 횟수만큼 반복해줌
		// 문법
		// for(초기식 ; 조건식 ; 증감식) { 실행문장; }
		// int i;
		for(int i=0;i<10;i++) {
			System.out.print(i);
		}
		System.out.println();
		for(int i=0;i <= 9;i++) {
			System.out.print(i);
		}
	}
}
